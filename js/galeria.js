    function generateCarCards(cars) {
      const carCardsContainer = document.getElementById('car-cards');
      carCardsContainer.innerHTML = '';
  
      for (const brand in cars) {
        cars[brand].forEach(car => {
          const card = `
          <div class="col-lg-4 col-md-6 mb-4">
          <div id="card-${car.id}" class="card">
            <img src="${car.image}" class="zoom" alt="${car.title}">
            <div class="card-body">
              <h5 class="card-title text-center"><strong>${car.title}</strong></h5>
              <div class="like-container">
              </div>
            </div>
          </div>
        </div>
        
        
        
        
  
        
        
          `;
          carCardsContainer.innerHTML += card;
        });
      }
    }
  
    generateCarCards(cars);
  
    function filterCars(brand) {
      if (brand === 'todos') {
        generateCarCards(cars);
      } else {
        const filteredCars = { [brand]: cars[brand] };
        generateCarCards(filteredCars);
      }
    }
  
    document.getElementById('car-select').addEventListener('change', function() {
      const selectedBrand = this.value;
      filterCars(selectedBrand);
    });
  
  
  
  
